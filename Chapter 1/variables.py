# variables in python

# String Variable
name = "Jamshaid"                            # variable
print("My name is" , name)                   # print it

# int variable
age = 15                                     # variable
print("I am", age , "years old")             # print it

# Float Variable
height = 5.64                                # variable
print("My Height is" , height)               # print it

# Boolean Variable
is_Student = True                            # variable
print("is i am a student?" , is_Student)     # print it

# None Variable
girlFriend = None                            # variable
print("have i a gf?" , girlFriend)           # print it

# More Variables in One Line
a, b, c = 1, 2, 3                            # variable
print( a , b , c)                            # print it
